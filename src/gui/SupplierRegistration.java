    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectRow = jTable1.getSelectedRow();
        jButton2.setEnabled(false);
        jTextField5.setText(String.valueOf(jTable1.getValueAt(selectRow, 0)));
        jTextField5.setEditable(false);
        jTextField2.setText(String.valueOf(jTable1.getValueAt(selectRow, 1)));
        jTextField3.setText(String.valueOf(jTable1.getValueAt(selectRow, 2)));
        jTextField1.setText(String.valueOf(jTable1.getValueAt(selectRow, 3)));
        jTextField4.setText(String.valueOf(jTable1.getValueAt(selectRow, 4)));

        if (evt.getClickCount() == 2) {
            if (grn != null) {
                grn.getjTextField2().setText(String.valueOf(jTable1.getValueAt(selectRow, 0)));
                grn.getjLabel15().setText(String.valueOf(jTable1.getValueAt(selectRow, 1)) + " " + (String.valueOf(jTable1.getValueAt(selectRow, 2))));
                this.dispose();
            }
        }

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `grn` INNER JOIN `grn_item` ON `grn`.`id`=`grn_item`.`grn_id`"
                    + "WHERE `grn`.`supplier_mobile`='" + String.valueOf(jTable1.getValueAt(selectRow, 0)) + "'");

            double total = 0;

            HashMap<Integer, Double> grns = new HashMap<>();

            while (resultSet.next()) {
                double qty = resultSet.getDouble("grn_item.qty");
                double buying_price = resultSet.getDouble("grn_item.buying_price");

                double itemTotal = qty * buying_price;

                //total = total+itemTotal
                total += itemTotal;

                grns.put(resultSet.getInt("grn.id"), resultSet.getDouble("grn.paid_amount"));
            }

            double totalPaid = 0;

            for (Double paid : grns.values()) {
                totalPaid += paid;
            }

            jLabel10.setText(String.valueOf(grns.size()));
            jLabel9.setText(String.valueOf(total - totalPaid));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

