    public void loadSuppliers(String col, String type, String search) {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `supplier`"
                    + " INNER JOIN `company` ON `supplier`.`company_id`=`company`.`id` WHERE `mobile` LIKE '" + search + "%' ORDER BY `" + col + "` " + type + "");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("LAST_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("name"));
                model.addRow(vector);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
        }

    }

