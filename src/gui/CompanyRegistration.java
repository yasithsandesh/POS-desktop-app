    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            String name = jTextField1.getText();
            String hotline = jTextField2.getText();
            int selectRow = jTable1.getSelectedRow();

            String id = String.valueOf(jTable1.getValueAt(selectRow, 0));
            String selectName = String.valueOf(jTable1.getValueAt(selectRow, 1));
            String selectHotline = String.valueOf(jTable1.getValueAt(selectRow, 2));

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Company Name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (hotline.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Company hoyline number", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!hotline.matches("/^(?:0|94|\\+94)?(?:(11|21|23|24|25|26|27|31|32|33|34|35|36|37|38|41|45|47|51|52|54|55|57|63|65|66|67|81|912)(0|2|3|4|5|7|9)|7(0|1|2|4|5|6|7|8)\\d)\\d{6}$/")) {
                JOptionPane.showMessageDialog(this, "Invalid Company hoyline number", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (selectName.equals(name) && selectHotline.equals(hotline)) {

                JOptionPane.showMessageDialog(this, "Please change name or hoyline update", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {

                ResultSet resultSet = MySQL.execute("SELECT * FROM `company` WHERE (`name`='" + name + "' OR `hotline`='" + hotline + "') AND `id`!='" + id + "' ");
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Company name or hotline alredy used", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.execute("UPDATE `company` SET `name`='" + name + "',`hotline`='" + hotline + "' WHERE `id`='" + id + "'");
                    loadCompany();
                    reset();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String name = jTextField1.getText();
            String hotline = jTextField2.getText();

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Company Name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (hotline.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Company hoyline number", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!hotline.matches("/^(?:0|94|\\+94)?(?:(11|21|23|24|25|26|27|31|32|33|34|35|36|37|38|41|45|47|51|52|54|55|57|63|65|66|67|81|912)(0|2|3|4|5|7|9)|7(0|1|2|4|5|6|7|8)\\d)\\d{6}$/")) {
                JOptionPane.showMessageDialog(this, "Invalid Company hoyline number", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                ResultSet resultSet = MySQL.execute("SELECT * FROM `company` WHERE `name`='" + name + "' OR `hotline`='" + hotline + "' ");
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "Company name or hotline alredy used", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.execute("INSERT INTO `company`(`name`,`hotline`)VALUES('" + name + "','" + hotline + "')");
                    loadCompany();
                    reset();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        jButton2.setEnabled(true);
        jButton1.setEnabled(false);
        jTable1.setEnabled(false);
        int selectRow = jTable1.getSelectedRow();
        jTextField1.setText(String.valueOf(jTable1.getValueAt(selectRow, 1)));
        jTextField2.setText(String.valueOf(jTable1.getValueAt(selectRow, 2)));

        if (evt.getClickCount() == 2) {
            String name = String.valueOf(jTable1.getValueAt(selectRow, 1));
            String id = String.valueOf(jTable1.getValueAt(selectRow, 0));
            sr.setCompanyName(name);
            sr.setCompanyId(id);
            sr.mobileFieldGrapFocus();
            this.dispose();
        }
    }//GEN-LAST:event_jTable1MouseClicked

