    private void loadCompany() {

        try {
            ResultSet companyRs = MySQL.execute("SELECT * FROM `company`");
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (companyRs.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(companyRs.getString("id"));
                vector.add(companyRs.getString("name"));
                vector.add(companyRs.getString("hotline"));
                model.addRow(vector);
                jTable1.setModel(model);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

